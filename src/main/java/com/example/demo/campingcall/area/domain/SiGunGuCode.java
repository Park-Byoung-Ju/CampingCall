	public static Integer getSiGunGuCode(String areaCode) {
		if(areaCode == null) {
			return null;
		}
		
		String[] split = areaCode.split("/");
		
		if(split[1] == null || split[1].equals("undefined")) {
			return null;
		}
		
		Integer siGunGu = Integer.parseInt(split[1]);
		
		
		return siGunGu;
	}
